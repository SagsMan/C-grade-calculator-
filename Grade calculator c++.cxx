#include<iostream>
using namespace std;
int main(){
	int score;
	//Grade calculator
	cout<<"Enter Score"<<endl;
	cin>>score;
	if (score>=70)
	cout<<"A"<<endl;
	else if (score>=60)
	cout<<"B"<<endl;
		else if (score>=50)
	cout<<"C"<<endl;
		else if (score>=45)
	cout<<"D"<<endl;
		else if (score>=40)
	cout<<"E"<<endl;
		else if (score>=39)
	cout<<"FAIL"<<endl;
	
	
return 0;}